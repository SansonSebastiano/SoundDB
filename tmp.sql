DROP TABLE IF EXISTS Utente CASCADE;
DROP TABLE IF EXISTS Artista CASCADE;
DROP TABLE IF EXISTS Podcaster CASCADE;
DROP TABLE IF EXISTS Album CASCADE;
DROP TABLE IF EXISTS Podcast CASCADE;
DROP TABLE IF EXISTS Canzone CASCADE;

CREATE TABLE Utente (
	mail varchar (50) primary key,
	nome varchar (50),
	cognome varchar (50),
	stato char (2),
	password varchar (30) not null,
	nickname varchar (20) not null unique,
	followers int,
	following int
);

CREATE TABLE Artista (
	mail varchar (50) primary key,
	nome varchar (50),
	cognome varchar (50),
	stato char (2),
	password varchar (30) not null,
	followers int,
	nome_artista varchar (30) not null unique,
	info varchar (200),
	label varchar (20),
	ascolti_mensili int
);

CREATE TABLE Podcaster (
	mail VARCHAR(50) primary key,
	nome VARCHAR(50),
	cognome VARCHAR(50),
	stato VARCHAR(50),
	password VARCHAR(50) not null unique,
	followers INT,
	nome_podcaster VARCHAR(30),
	info VARCHAR(200)
);

CREATE TABLE Album (
	album_id SERIAL primary key,
	titolo varchar (30),
	artista varchar (50),
	data_pubblicazione date,
	FOREIGN KEY (artista) REFERENCES Artista(mail)
);

CREATE TABLE Podcast (
	nome_podcast VARCHAR(30),
	podcaster VARCHAR(50),
	info VARCHAR(100),
	primary key (nome_podcast, podcaster),
	FOREIGN KEY (podcaster) REFERENCES Podcaster(mail)
);

CREATE TABLE Canzone (
	titolo varchar (30),
	album int,
	durata int,
	primary key (titolo, album),
    FOREIGN KEY (album) REFERENCES Album(album_id)
);

